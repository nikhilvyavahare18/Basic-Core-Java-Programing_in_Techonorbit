
public class Array_Demo 
{
	public static void main(String args[])
	{
		int array[]= {5,6,7,8,9};
		
		System.out.println(array);//print the base address
		
		System.out.println(array[3]);//print the size of index 3
		
		System.out.println(array.length); //print the whole length of an array
		
		String [] salary= {"nikhil","nikita"};
		System.out.println(salary);//print the base address
	}

}
